async def async_setup_entry(
    hass: HomeAssistant,
    entry: ConfigEntry,
    async_add_entities: AddEntitiesCallback,
) -> None:
    """Set up Dreame Vacuum sensor based on a config entry."""
    coordinator: ImmichDataUpdateCoordinator = hass.data[DOMAIN][entry.entry_id]
    async_add_entities(
        ExampleSensor(coordinator, "description")
    )